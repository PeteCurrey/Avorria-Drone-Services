import { industriesData } from '@/lib/industries-data'
import SectorLayout from '@/components/sectors/SectorLayout'
import { Metadata } from 'next'
import { notFound } from 'next/navigation'

export const metadata: Metadata = {
  title: 'Drone Surveys for Heritage & Conservation | Altitude Hire',
  description: 'Specialist drone services for heritage sites. High-resolution building documentation, listed structure audits and digital twin preservation.',
}

export default function HeritageSector() {
  const data = industriesData.find(d => d.slug === 'heritage-conservation')
  if (!data) notFound()

  return (
    <SectorLayout
      slug={data.slug}
      title={data.title}
      hero={data.hero}
      challenge={data.challenge}
      outcomes={data.outcomes}
      workflow={data.workflow}
      serviceStack={data.serviceStack}
      bundles={data.recommendedBundles}
      deliverables={data.deliverables}
      missionProfiles={data.missionProfiles}
      resources={data.resources}
      faqs={data.faqs}
      heroVideo="/videos/inspection.mp4"
    />
  )
}
